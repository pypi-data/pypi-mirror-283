    string.endswith(str)	#Заканчивается ли строка string шаблоном str
    string.join([1, 2, 3])	#Сборка строки из списка с разделителем string
    string.capitalize()	#Переводит первый символ строки в верхний регистр, а все остальные в нижний
    string.count(str)	#Возвращает количество непересекающихся вхождений подстроки в диапазоне [начало, конец] (0 и длина строки по умолчанию)
