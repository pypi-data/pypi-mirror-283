    string.find(str)	#Поиск подстроки в строке. Возвращает номер первого вхождения или -1
    string.rfind(str)	#Поиск подстроки в строке. Возвращает номер последнего вхождения или -1
    string.index(str)	#Поиск подстроки в строке. Возвращает номер первого вхождения или вызывает ValueError
    string.rindex(str)	#Поиск подстроки в строке. Возвращает номер последнего вхождения или вызывает ValueError
